#example for functions in python
num=[10,20,30,40,50]
#len()--------->returns the number of elements in list
print("length of list is:",len(num))
#max()--------->returns the maximum value from the list
print("maximum number is:",max(num))
#min()--------->returns the minimum value from the list
print("minimum number is:",min(num))
#sum()--------->returns summation of elements
print("summation is:",max(num))
#sorted()------->returns the sorted list
print("sorted list is:",sorted(num))
print(any(num))
print(all(num))